# Q: What happens when you try to use the + operator on different/mixed types (i.e. string + list, list + tuple)?
# A: Traceback error

# Q: Does + work when one of the operands is a dictionary?
# A: No

# Q: Does the append method work for both lists and strings?  How about using the keys method on lists?
# A: No and no.

# Q: Finally, what type of object do you get back when you slice or concatenate two lists or two strings?
# A: The same type as what you started with (i.e. concatenating two lists would return a list).